package org.archstudio.graphlayout;

public class GraphLayoutException extends Exception {

	private static final long serialVersionUID = -7180849776609895945L;

	public GraphLayoutException() {
		super();
	}

	public GraphLayoutException(String message, Throwable cause) {
		super(message, cause);
	}

	public GraphLayoutException(String message) {
		super(message);
	}

	public GraphLayoutException(Throwable cause) {
		super(cause);
	}

}
