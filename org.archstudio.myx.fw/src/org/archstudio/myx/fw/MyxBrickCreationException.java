package org.archstudio.myx.fw;

public class MyxBrickCreationException extends Exception {

	private static final long serialVersionUID = 9114494360756398398L;

	public MyxBrickCreationException() {
		super();
	}

	public MyxBrickCreationException(String message) {
		super(message);
	}

	public MyxBrickCreationException(Throwable cause) {
		super(cause);
	}

	public MyxBrickCreationException(String message, Throwable cause) {
		super(message, cause);
	}

}
